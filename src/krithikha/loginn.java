    String login(String u,String pwd){
        String q="";
    try{ String c="Select * from pwd";
            if(u.equals("")||pwd.equals(""))
            {q="";}
                
            else{     
            Statement st=con.createStatement();
            ResultSet  rs=st.executeQuery(c);

            while(rs.next()){
                String   a=rs.getString("username");
                String p=rs.getString("password");

                if(a.equalsIgnoreCase(u) && p.equalsIgnoreCase(pwd)){
                     q="yes";
                   
                    break; }
                else 
                    q="Your username and password do not match. Please try again.";
                    
                }
            }       
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error in connection");

        }
     return q;   }
    

